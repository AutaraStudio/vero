interface SocialIconProps {
  platform: string
  size?: number
  className?: string
  ariaHidden?: boolean
}

/**
 * Render the brand-glyph SVG for a platform. Returns null if the platform
 * isn't in our map — caller should also gate the rendering of the link
 * itself so we don't render a clickable empty box.
 */
export function SocialIcon({ platform, size = 18, className, ariaHidden = true }: SocialIconProps): ReactNode {
  const path = PATHS[platform.toLowerCase()]
  if (!path) return null
  return (
    <svg
      width={size}
      height={size}
      viewBox="0 0 24 24"
      fill="currentColor"
      className={className}
      aria-hidden={ariaHidden}
    >
      <path d={path} />
    </svg>
  )
}

/** Display label for a platform (used in aria-label). */
export function platformLabel(platform: string): string {
  const map: Record<string, string> = {
    linkedin: 'LinkedIn',
    youtube: 'YouTube',
    instagram: 'Instagram',
    facebook: 'Facebook',
    x: 'X',
    tiktok: 'TikTok',
    threads: 'Threads',
    bluesky: 'Bluesky',
  }
  return map[platform.toLowerCase()] ?? platform
}

